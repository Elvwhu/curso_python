###
# 03 - casting
# Python permite convertir entre tipos de datos
### 

print("int:") # Casting to int
print(int(10.5))
print(int("123"))
print(int(True))
print(int(False))

print("float:") # Casting to float
print(float(10))
print(float("123.45"))
print(float(True))
print(float(False))

print("string:") # Casting to string
print(str(10))
print(str(10.5))
print(str(True))
print(str(False))

print("bool:") # Casting to bool
print(bool(0))
print(bool(1))
print(bool(""))
print(bool("Hello, World!"))
