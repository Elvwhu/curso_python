print("Hello, World!") # This is a simple Python script that prints "Hello, World!" to the console.
print('Esto también funciona con comillas simples') # You can also use single quotes for strings.

# Separador en print
print("Python", "es", "la", "ostia", sep="-") # The 'sep' parameter allows you to specify a separator between the printed items.

# Parametro end
print("Python", "es", "la", "ostia", end=" ") # The 'end' parameter allows you to specify what to print at the end of the output. By default, it is a newline character.